# ชนิดข้อมูลที่เห็นบ่อย
Str = "ตัวอักษรข้อความ" # string
Num = 1 # number แบ่งได้ 2 ประเภท คือ 
Num = 10 # integer จำนวนเต็ม  ||  หรือ จำนวนเต็มลบ ก็ได้ -1 , -2 , -421, ... 
Num = 834.34 # float จำนวนทศนิยม 
Bool = True # Boolean ใน python  ค่า True และ False จะต้องขึ้นด้วยตวพิมใหญ่เสมอ

# การ check datatype ของข้อมูลด้วย type()
print(type(Str)) # <class 'str'>
print(type(20)) # <class 'int'>
print(type(Num)) # <class 'float'>
print(type(Bool)) # <class 'bool'>

